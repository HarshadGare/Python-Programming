

for x in range(5):  # range(stop)
    print(x)

for x in range(4, 9):   # range(start, stop)
    print(x)

for x in range(1, 15, 2):   # range(start, stop, step)
    print(x)

for a in [2, 4, 3, 7, 8]:
    print(a)

# for Loop with Else part
for x in range(5):  # range(stop)
    print(x)
else:
    print("Value of x is Exceed")

