
s1 = {1, 2, 3, 8, 4, 3, 6, 2}
print("s1 :- ", s1)
s2 = {'a', 'b', 45, 'g', 7, 9, 'b', 2, 7, 'e', 'g'}
print("s2 :- ", s2)

# *********** OPERATIONS ON SET ***************
print("Union s1 | s2 :- ", s1 | s2)
print("Insertion s1 & s2 :- ", s1 & s2)
print("Difference s1 - s2 :- ", s1 - s2)
print("Difference s2 - s1 :- ", s2 - s1)
